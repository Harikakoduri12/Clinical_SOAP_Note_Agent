"""
Synthetic clinical encounter generator.

Two ideas make this work:

1. BACKWARDS GENERATION. We define the correct SOAP note FIRST (from a fixed set
   of clinical scenarios with known ICD codes), then generate the conversation
   that would have produced it. Because the transcript is built from the note,
   the (transcript -> gold note) pairing is correct by construction and needs no
   hand-labeling. This gives us an authoritative gold set for free.

2. KNOWN PHI INJECTION. Faker produces the fake identities, and we KEEP the exact
   values we injected. That lets the eval measure redaction recall and output
   leakage exactly, because we know precisely what PHI is present in each case.

In production the conversation text would be generated by an LLM for richer,
messier dialogue. Here we template it deterministically (seeded) so the demo is
reproducible and runs with no API. The *structure* — note-first, known PHI,
gold pairing — is identical to the LLM version.
"""

